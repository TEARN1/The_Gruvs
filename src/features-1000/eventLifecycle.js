/**
 * Get all lifecycle features
 */
export const getAllLifecycleFeatures = () => {
  return {
    beforeEvent: BEFORE_EVENT_FEATURES,
    duringEvent: DURING_EVENT_FEATURES,
    afterEvent: AFTER_EVENT_FEATURES
  };
};

/**
 * Get features by stage
 */
export const getFeaturesByStage = (stage) => {
  const stages = {
    before: BEFORE_EVENT_FEATURES,
    during: DURING_EVENT_FEATURES,
    after: AFTER_EVENT_FEATURES
  };
  return stages[stage] || null;
};

/**
 * Get features by type within stage
 */
export const getFeaturesByType = (stage, type) => {
  const features = getFeaturesByStage(stage);
  return features ? features[type] : null;
};

/**
 * Search lifecycle features
 */
export const searchLifecycleFeatures = (query) => {
  const results = [];
  const allFeatures = getAllLifecycleFeatures();

  Object.entries(allFeatures).forEach(([stage, stageFeatures]) => {
    Object.entries(stageFeatures).forEach(([type, typeFeatures]) => {
      if (typeof typeFeatures === 'object' && typeFeatures.features) {
        typeFeatures.features.forEach(feature => {
          if (feature.toLowerCase().includes(query.toLowerCase())) {
            results.push({ stage, type, feature });
          }
        });
      }
    });
  });

  return results;
};

/**
 * Get total feature count
 */
export const getTotalLifecycleFeatureCount = () => {
  return EVENT_LIFECYCLE_STATS.totalFeatures;
};

